/* Understanding Objects
Objects are key-value pairs, where the key is a string and the value can be of any data type.
*/

// Creating an object
let person = {
    name: "Sai Prasad",
    age: 24,
    hobbies: ["Coding", "Reading", "Traveling"]
}

// Accessing properties
console.log(person.name);
// Other method
console.log(person["name"]);